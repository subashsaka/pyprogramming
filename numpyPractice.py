import numpy as np

# Create a numpy array
arr = np.array([1, 2, 3, 4, 5])

# Print the array
print("Array:", arr)

# Calculate the mean of the array
mean = np.mean(arr)
print("Mean of the array:", mean)

# Calculate the sum of the array
sum_arr = np.sum(arr)
print("Sum of the array:", sum_arr)

# Create a 2x3 matrix with all elements as 1
matrix = np.ones((2, 3))
print("2x3 Matrix of ones:\n", matrix)
